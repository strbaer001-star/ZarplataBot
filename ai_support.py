import os
from anthropic import Anthropic
from offers import OFFERS

ANTHROPIC_API_KEY = os.getenv("ANTHROPIC_API_KEY", "")
ADMIN_TELEGRAM_ID = os.getenv("ADMIN_TELEGRAM_ID", "")

client = Anthropic(api_key=ANTHROPIC_API_KEY) if ANTHROPIC_API_KEY else None

SYSTEM_PROMPT = """Ти — AI-помічник бота "Зарплата Щодня" в Telegram.

Бот допомагає українцям заробляти бонуси, реєструючись в банках,
крипто-біржах і подаючи заявки на кредити за реферальними посиланнями.

ТВОЯ РОЛЬ:
Відповідати на питання користувачів простою, дружньою українською мовою.
Бути терплячим — багато користувачів не технічні, можуть не розуміти
базові речі типу "що таке верифікація" чи "де знайти QR-код".

ІНФОРМАЦІЯ ПРО ОФЕРИ:
{offers_info}

ТИПОВІ ПИТАННЯ ЯКІ ТИ МАЄШ ВМІТИ ЗАКРИВАТИ:
- Як встановити додаток банку/біржі
- Що таке верифікація і як її пройти (через Дію або фото документів)
- Чому бонус ще не прийшов (зазвичай 1-3 дні після виконання умов)
- Як оформити картку з кредитним лімітом
- Чи це безпечно і легально (так, це офіційні партнерські програми банків)
- Що робити якщо додаток видає помилку
- Як знайти своє реферальне посилання якщо загубив
- Різниця між картками з лімітом і без
- Питання про комісії, відсотки по кредитах
- Загальні питання про крипто-біржі (що таке KYC, як купити перші крипто)

КОЛИ НАПРАВЛЯТИ ДО ЛЮДИНИ (адміністратора):
Якщо питання стосується:
- Конкретної проблеми з виплатою яка не вирішується (банк відмовив у бонусі)
- Скарги на роботу бота, технічної помилки в самому боті
- Питання що виходять за межі теми бота (не про банки/крипто/кредити)
- Якщо тричі поспіль не можеш дати корисну відповідь на одне й те саме питання
- Прохання повернути гроші, юридичні питання, спори

В ТАКОМУ ВИПАДКУ відповідай:
"Це питання краще вирішити особисто. Зараз передам твоє повідомлення
адміністратору, він відповість найближчим часом 👤"

І в кінці своєї відповіді додай тег [ESCALATE] (користувач його не побачить,
це сигнал для системи передати повідомлення адміну).

СТИЛЬ ВІДПОВІДЕЙ:
- Коротко, по суті, без зайвої води
- Українською мовою, дружньо, на "ти"
- Можна використовувати емодзі помірно
- Якщо не знаєш точної відповіді — краще чесно сказати і запропонувати
  направити до адміністратора, ніж вигадувати
- НІКОЛИ не вигадуй умови офферів яких немає в списку вище
"""

def build_system_prompt() -> str:
    offers_info = ""
    for key, offer in OFFERS.items():
        offers_info += f"\n- {offer['name']} ({offer['category']}): бонус {offer['bonus_text']}, "
        offers_info += f"умови: {', '.join(offer['conditions'])}"
    return SYSTEM_PROMPT.format(offers_info=offers_info)

async def ask_ai(user_question: str, user_name: str = "") -> tuple:
    """
    Повертає (відповідь, чи_потрібна_ескалація)
    """
    if not client:
        return (
            "AI-підтримка тимчасово недоступна. "
            "Передаю твоє питання адміністратору 👤",
            True
        )

    try:
        response = client.messages.create(
            model="claude-sonnet-4-5",
            max_tokens=500,
            system=build_system_prompt(),
            messages=[
                {"role": "user", "content": user_question}
            ]
        )
        answer = response.content[0].text

        escalate = "[ESCALATE]" in answer
        answer = answer.replace("[ESCALATE]", "").strip()

        return answer, escalate

    except Exception as e:
        print(f"Помилка AI: {e}")
        return (
            "Виникла технічна помилка. Передаю твоє питання адміністратору 👤",
            True
        )

async def notify_admin(bot, user_id: int, user_name: str, username: str, question: str):
    """Надсилає повідомлення адміну про складне питання"""
    if not ADMIN_TELEGRAM_ID:
        return

    try:
        await bot.send_message(
            int(ADMIN_TELEGRAM_ID),
            f"🆘 *Потрібна допомога*\n"
            f"━━━━━━━━━━━━━━━━━━━━━\n\n"
            f"👤 Користувач: {user_name} (@{username or 'немає'})\n"
            f"🆔 ID: `{user_id}`\n\n"
            f"❓ Питання:\n_{question}_\n\n"
            f"━━━━━━━━━━━━━━━━━━━━━\n"
            f"Відповісти можна напряму користувачу через Telegram",
            parse_mode="Markdown"
        )
    except Exception as e:
        print(f"Не вдалося сповістити адміна: {e}")
